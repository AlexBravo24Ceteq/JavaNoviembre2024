package com;

public class EstructuraCiclos {

	public static void main(String[] args) {

		//Estructura ciclos, son tambien llamadas Bucles o estructuras de iteracion
		
		System.out.println("Tabla de multiplicar del 12");

		for(int i = 0; i<=10;i++) {
			System.out.println("12 X " + (i) + " = " + (12*i));
		}
		
		

	}

}
